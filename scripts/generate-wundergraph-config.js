/**
 * Main function
 */
async function main() {
  console.log('🚀 WunderGraph Configuration Generator');
  console.log('======================================\n');
  
  // Read api-config.yaml
  console.log('📖 Reading api-config.yaml...');
  if (!fs.existsSync(API_CONFIG_PATH)) {
    console.error('❌ Error: api-config.yaml not found');
    process.exit(1);
  }
  
  const yamlContent = fs.readFileSync(API_CONFIG_PATH, 'utf8');
  const config = yaml.load(yamlContent);
  console.log(`   Found ${config.apis?.length || 0} API definitions`);
  console.log(`   Found ${Object.keys(config.types || {}).length} type definitions\n`);
  
  // Ensure directories exist
  console.log('📁 Ensuring directories exist...');
  if (!fs.existsSync(WUNDERGRAPH_DIR)) {
    fs.mkdirSync(WUNDERGRAPH_DIR, { recursive: true });
  }
  if (!fs.existsSync(OPERATIONS_DIR)) {
    fs.mkdirSync(OPERATIONS_DIR, { recursive: true });
  }
  console.log('   ✓ wundergraph/ directory exists');
  console.log('   ✓ wundergraph/operations/ directory exists\n');
  
  // Generate type definitions
  console.log('📝 Generating TypeScript type definitions...');
  const typesContent = generateTypeDefinitions(config);
  const typesPath = path.resolve(WUNDERGRAPH_DIR, 'types.ts');
  fs.writeFileSync(typesPath, typesContent);
  console.log(`   ✓ Generated ${typesPath}\n`);
  
  // Generate WunderGraph configuration
  console.log('⚙️  Generating WunderGraph configuration...');
  const configContent = generateDataSourceConfig(config);
  const configPath = path.resolve(WUNDERGRAPH_DIR, 'wundergraph.config.ts');
  fs.writeFileSync(configPath, configContent);
  console.log(`   ✓ Generated ${configPath}\n`);
  
  // Generate GraphQL operations
  console.log('🔧 Generating GraphQL operations...');
  const operations = generateOperations(config);
  for (const op of operations) {
    const opPath = path.resolve(OPERATIONS_DIR, `${op.name}.graphql`);
    fs.writeFileSync(opPath, op.content);
    console.log(`   ✓ Generated ${op.name}.graphql`);
  }
  console.log();
  
  // Generate OpenAPI spec
  console.log('📄 Generating OpenAPI specification...');
  const openapiSpec = generateOpenApiSpec(config);
  const openapiPath = path.resolve(WUNDERGRAPH_DIR, 'openapi.yaml');
  fs.writeFileSync(openapiPath, yaml.dump(openapiSpec, { indent: 2 }));
  console.log(`   ✓ Generated ${openapiPath}\n`);
  
  // Generate test file
  console.log('🧪 Generating test file...');
  const testContent = generateTestFile(config);
  const testPath = path.resolve(WUNDERGRAPH_DIR, 'generate-wundergraph-config.test.js');
  fs.writeFileSync(testPath, testContent);
  console.log(`   ✓ Generated ${testPath}\n`);
  
  console.log('✅ Generation complete!');
  console.log('\n📊 Summary:');
  console.log(`   - Type definitions: ${Object.keys(config.types || {}).length + (config.apis?.length || 0)} interfaces`);
  console.log(`   - Operations: ${operations.length} files`);
  console.log(`   - OpenAPI spec: 1 file`);
  console.log(`   - Test file: 1 file`);
  console.log('\n📝 Next steps:');
  console.log('   1. Install dependencies: npm install js-yaml');
  console.log('   2. Run generator: node scripts/generate-wundergraph-config.js');
  console.log('   3. Run tests: node wundergraph/generate-wundergraph-config.test.js');
}

// Run main function
main().catch(error => {
  console.error('❌ Error:', error.message);
  console.error(error.stack);
  process.exit(1);
});
